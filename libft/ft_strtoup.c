#include "libft.h"

void	ft_strtoup(char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'f')
			str[i] = ft_toupper(str[i]);
		++i;
	}
}
