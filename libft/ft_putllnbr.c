#include "libft.h"

void	ft_putllnbr(long long n)
{
	char	c;

	if (n == -9223372036854775807 - 1)
	{
		ft_putstr("9223372036854775808");
		return ;
	}
	if (n < 0)
		n *= -1;
	if (n >= 10)
		ft_putllnbr(n / 10);
	c = n % 10 + '0';
	ft_putchar(c);
}
